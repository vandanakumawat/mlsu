          <p class="content">
            <b>Department of Physics</b>, Mohanlal Sukhadia University, Udaipur was established in 1965 The department offers postgraduate and undergraduate courses in physics and has several active research groups. Many students graduated from this department are in eminent positions in education, research and industry. The department also offers doctoral programme in Physics, leading to Ph.D degree. The major areas of research conducted in the department include Atomic and nuclear Physics, Solid State Physics, Ionospheric and Atmospheric Physics, Material Science and Astrophysics. The department has been identified by University Grants Commission (UGC) under DSA with thrust areas as Hyperfine Interactions, X-ray scattering,  Atmospheric and Astro Physics. The Department is under the DST-FIST program and was also supported by the UGC COSIST program. Research programs in each group is also getting research support under research projects from central agencies like UGC, DST, DRDO, ISRO, etc. Faculty members also have active collaborations with other universities and institutions within and outside the country. 

         <p class="content"> 
           The department of physics is also playing a vital role in promoting teaching and research in interdisciplinary areas of physics, electronics, information technology, life sciences and geology. The M. Sc.(Electronics) and M. Sc.(Information Technology) courses are offered by the department mainly to strengthen research in the interdisciplinary area of physics, communication and computational sciences. Studies of minerals from Aravalli region, X-ray diffraction studies in biological samples, synthesis of organic and inorganic crystals and studies of environmental samples are also undertaken by the department to promote interface of physics with geological, biological, chemical and environmental sciences.
        </p>
