          <p class="content">
          The University <b>Department of Geology</b>, Udaipur was founded in June 1950 as the first post-graduate Science department in Rajasthan. Dr. K.P. Rode was the Founder Professor of the Department. Through these six decades the department has produced large number of Geologists and successfully completed several research projects funded by various national and international agencies. The department has made significant contribution in the study of Precambrian Geology of Rajasthan with respect to its Petrology, Structural Geology & Tectonics, mineral resources. Department has also made noteworthy contribution in field of Micropaleontology specially in Ostracods and Biostratigraphy. The department has successfully organized number of national seminars, symposia, workshops and advance courses.<br/>

          Because of its contribution in teaching and research, the Department also received the support of University Grant Commission through COSIST and SAP (Four times) and Department of Science & Technology through FIST.<br/>

          The Department has its own library, having more than 6500 books and journals. It has a very well stocked and popular “Geology Museum”’ that was named as “Rode Museum” in honour of the founder Professor K.P. Rode.<br/>

          The department is proud on its 12 alumni who have received a very prestigious National Mineral Award. A number of its alumni served and serving on very senior positions in GSI, ONGC, AMD, CGWB, DMG etc.
