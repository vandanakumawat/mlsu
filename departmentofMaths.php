          <p class="content">
           The <b>Department of Mathematics</b> was established since the inception of University in 1964. Later on, in the year of 1989-90, this Department was renamed as Department of Mathematics & Statistics on account of opening of post-graduate courses in Statistics. Hence, the Department offers two post graduate programmes in Mathematics and Statistics separately.  Three years under-graduate programmes are also run by the Department through University College of Science in both the subjects. The Department is equipped with Internet Laboratory, Computer Laboratory and a rich library besides core faculty.  The Department welcomes students from India and abroad to pursue courses in M.A./M.Sc. Mathematics/ Statistics. Outstation candidates are normally provided hostel accommodation. Students on completion of their courses find placements in Governments, various institutions like Education, Banking, Insurance, Information Technologies and many other industrial organizations.<br/>

           The Department also offers Doctoral Degree programme in both the subjects leading to Ph.D. Degree. The major areas of research include Theory of Relativity, Astronomy, Special Functions, Algebra, Fractional Calculus, Analysis in Mathematics and Statistical Inference, Design of Experiments, Sampling Theory, Data Mining, Bio-Statistics, Operations Research in Statistics. The Department also promotes interdisciplinary research programmes as well.
