          <h5 class="subheading" style="margin-top:2%"><b >History and Landmark</b></h5>
           <p class="content" style="margin-top:3%">
            The <b>Department of Library and Information Science</b> was started in the year 1975 as a separate department in the University college of Social Science & Humanities with the able leadership of Dr. V.B. Nanda.Later in the year 1984 two permanent Asst. Prof. namely Dr. L.N. Vermaand Dr. T. D. Tilwani were recruited permanently in the department. In the year 1994 the department was upgraded to a PG dept. as Master in LIS course was started as a regular one year programme. Subsequently in the session 1999, the facilities of Ph.Dprogramme in LIS were initiated in the dept and this is considered as second full- fledges dept. having facility of UG, PG, &Ph.D in the Rajasthan State.
          </p>
          <b class="subheading"> Academic Status </b>
          <p class="content" style="margin-top:1%">
            This department is second in the whole state of Rajasthan by which all the courses of LIS from Bachelor degree to Ph.D are being run. As such it is a full fledges dept. after  University of Rajasthan, Jaipur, however this department taken lead over the department of LIS at university of Rajasthan, Jaipur so for implementation of Model Curriculum, 2002, released by UGC with the aim to maintain uniformity among the curriculum of different courses run by different universities. And also this is the first dept in the state in which high-tech papers on IT Theory as Practical are offered at UG and PG Level along with well equipped computer lab having 20 PCs and three Laser Printers.
          </p>
          <b class="subheading"> Employment Status  </b>
          <p class="content" style="margin-top:1%">
            This department is pioneer in producing human resources with expertise in IT applications in Libraries and Information Centers and so for PG (M. Lib. I. Sc) students are concern there is about 100% placement while the UG (B. Lib. I. Sc) employment ratio is about 60%.
          </p>
