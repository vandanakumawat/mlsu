          <p class="content">
           <b> The Department of English</b> was established in 1959. Beginning with basic comes in English Literature; the department diversified its areas into American Literature, Translation and Gender Studies. Many eminent Visiting Professors and scholars from India and abroad have been visiting the department and contributing to the growth of the students and the faculty. The department has adopted the new model curriculum of UGC for Under Graduate (Pass & Honours), Post Graduate and M.Phil courses. The students have achieved excellent results in NET, SLET, RPSC and RAS competitive examinations.<br/>
          The Department has a well-equipped Language Lab with multi-media audio-visual systems and computers. In addition there is a library which stocks relevant books on literature and language. Apart from imparting instruction at the Masters and the Undergraduate (Pass course and Honours) levels, research work on a large scale in new and challenging areas is being conducted by the faculty as well as the students<br/>
          The Department organizes curricular and extra-curricular activities for the students, including Quiz Contests, Seminars, Declamation Contests and Group Discussions. In addition, the Department brings out a refereed research Journal that is Sukhadia University Journal of English Studies annually. The faculty of the Department is actively engaged in research pursuits and each member has authored several books and research papers. Over the years the thrust areas of research have been the various aspects of Indian Writing in English, Post Colonial Writings, Partition literature, War Literary Writings and Comparative Literature.
