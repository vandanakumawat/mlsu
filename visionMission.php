            2.To provide up-to-date, relevant and need based knowledge.<br/>
            3.To utilize the acquired knowledge in solving problem and innovation through research and development.<br/>
            4.To integrate latest technology such as Information and Communication Technology with teaching, research, extension and governance.<br/>
