          <p class='content'>
            Mohanlal Sukhadia University (erstwhile Udaipur University) at Udaipur is a State University established by an Act in the year 1962 to cater the needs of higher education in Southern Rajasthan with more then 2.25 Lakh Students. The University is located in Aravalli Hill Area largely dominated by tribal populations. Endowed with rich cultural heritage, natural resources and beautiful landscape, Udaipur is a world renowned tourist attraction.<br/>
            Ever since its inception university has been striving to maintain excellence in teaching, research and community service. Great emphasis has been laid in creating scientific temper, maintaining high ethical values and in keeping pace with emerging areas of higher learning. University has ensured overall socio-economic growth of all the sections of society by encouraging greater access and inclusive approach making it most preferred institution for higher education, learning and research.
          </p>
            <p class='content'>
            Conscious about its social responsibilities, the university has played significant role in socio-economic development of backward, under-privileged and socially challenged people through its various extension activities. UGC sponsored “Women’s Study Centre” and Ministry of Health and Family Welfare supported “Population Research Centre” established in the university have contributed significantly in the areas of women empowerment, gender equality and child development.
          </p>
           <p class='content'>
            University can take the pride in having ICT enabled working in most of its functional units involved in teaching, learning, research, administration and governance. A very strong infrastructure for e-libraries has enhanced academic growth of the faculty and students.
          </p>
          <p class='content'>
            Regular review of existing courses and introduction of new courses of current national and international relevance have been a major activity of the university to produce human resource which is more skilled and employable. Emphasis has been laid on inter-disciplinary and emerging technologies. To maintain high quality, the teaching and learning processes have been made more rigorous and effective. Evaluation process has been made more transparent and credible.
          </p>
          <p class='content'>
            Creation of new knowledge through research is one of the major objectives of higher education. Realizing its role in creation of new knowledge, the university has not only made visible impact on national and international levels but has also attracted interest of other institutions for collaborative research. Recognition of the Department of Botany, Geology, Physics and Zoology by UGC for its ‘Special Assistance Programme’ and support received from DST under FIST programme to various Science Departments is the testimony of scientific advances made by the faculty members.
          </p>
          <p class='content'>
            The university has always felt concerned about increasing access of students from various sections of the society to higher education. By providing reservations, financial aids, scholarships and relaxation in qualifications for socially backward classes, the university has registered significant increase in access during last few years. The university aims to achieve its goal of providing higher education to create just, plural and equitable society in consonance with constitutional values.
          </p>
